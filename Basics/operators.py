# Arithmetic Operators
addition = 5 + 3
subtraction = 7 - 2
multiplication = 4 * 6
division = 10 / 2
modulus = 15 % 4 #Output 3 15/4 Remainder 3
exponentiation = 2 ** 3

# Comparison Operators
is_equal = (5 == 5)
is_not_equal = (3 != 7)
greater_than = (10 > 5)
less_than_or_equal = (8 <= 10)

# Assignment Operators
x = 10
x += 5  # Equivalent to: x = x + 5

# Logical Operators
is_true = True
is_false = False
logical_and = (is_true and is_false)
logical_or = (is_true or is_false)
logical_not = not is_true

# Membership Operators
fruits = ["apple", "banana", "cherry"]
is_in_list = "banana" in fruits

# Identity Operators
x = 10
y = 10
are_same = (x is y)
